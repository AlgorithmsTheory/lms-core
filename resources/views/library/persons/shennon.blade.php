<p><strong>Клод Шеннон</strong></p>

<p>Клод Э́лвуд Ше́ннон &mdash; американский инженер и математик, его работы являются синтезом математических идей с конкретным анализом чрезвычайно сложных проблем их технической реализации.</p>

<p>Он является основателем теории информации, нашедшей применение в современных высокотехнологических системах связи. Шеннон внес огромный вклад в теорию вероятностных схем, теорию автоматов и теорию систем управления &mdash; области наук, входящие в понятие кибернетика. В 1948 году предложил использовать слово &laquo;бит&raquo; для обозначения наименьшей единицы информации (в статье &laquo;Математическая теория связи&raquo;).</p>

<p>Клод Шеннон родился 30 апреля 1916 года в городе Петоцки, штат Мичиган, США. Первые шестнадцать лет своей жизни Клод провел в Гэйлорде, Мичиган, где в 1932 году он закончил общеобразовательную среднюю школу Гэйлорда. В юности он работал курьером службы Western Union. Отец его был адвокатом и в течение некоторого времени судьей. Его мать была преподавателем иностранных языков и впоследствии стала директором Гэйлордской средней школы. Молодой Клод увлекался конструированием механических и автоматических устройств. Он собирал модели самолетов и радиотехнические цепи, создал радиоуправляемую лодку и телеграфную систему между домом друга и своим домом. Временами ему приходилось исправлять радиостанции для местного универмага. Томас Эдисон был его дальним родственником.</p>

<p>В 1936 г. Клод оканчивает Мичиганский университет, получив степень бакалавра по двум специальностям математика и электротехника, и устраивается в Массачусетский технологический институт, где он работал ассистентом-исследователем на дифференциальном анализаторе Ванневара Буша &mdash; аналоговом компьютере. Изучая сложные, узкоспециальные электросхемы дифференциального анализатора, Шэннон увидел, что концепции Буля могут получить достойное применение. Статья, написанная с его магистерской работы 1937 года &laquo;Символический анализ реле и коммутаторов&raquo;, была опубликована в 1938 году в издании Американского института инженеров-электриков (AIEE). Она также стала причиной вручения Шэннону премии Американского института инженерии имени Альфреда Нобеля в 1940 году. Цифровые цепи &mdash; это основа современной вычислительной техники, таким образом результаты его работ являются одними из наиболее важных научных результатов ХХ столетия. Говард Гарднер из Гарвардского университета отозвался о работе Шэннона, как о &laquo;возможно, самой важной, а также самой известной магистерской работе столетия&raquo;.</p>

<p>Докторская диссертация Шеннона, получившая название &laquo;Алгебра для теоретической генетики&raquo;, была завершена весной 1940 года. Шеннон получает Докторскую степень по математике и степень магистра по электротехнике.</p>

<p>В период с 1941 по 1956 гг. Шеннон преподает в Мичиганском университете и работает в компании Белл (Bell Labs). В лаборатории Белл Шеннон, исследуя переключающие цепи, обнаруживает новый метод их организации, который позволяет уменьшить количество контактов реле, необходимых для реализации сложных логических функций. Он опубликовал доклад, названный &laquo;Организация двухполюсных переключающих цепей&raquo;. Шеннон занимался проблемами создания схем переключения, развил метод, позволяющий создавать схемы, которые были надежнее, чем реле, из которых они были составлены. В конце 1940 года Шеннон получил Национальную научно-исследовательскую премию. Весной 1941 года он вернулся в компанию Белл. С началом Второй мировой войны Т.Фрай возглавил работу над программой для систем управления огнем для противовоздушной обороны. Шеннон работал над устройствами, засекавшими самолеты противника и нацеливавшими зенитные установки, также он разрабатывал криптографические системы, в том числе и правительственную связь, которая обеспечивала переговоры Черчилля и Рузвельта через океан. Как говорил сам Шеннон, работа в области криптографии подтолкнула его к созданию теории информации.</p>

<p>С 1950 по 1956 Шеннон занимался созданием логических машин, таким образом, продолжая начинания фон Неймана и Тьюринга. Он создал машину, которая могла играть в шахматы. В 1952 Шеннон создал обучаемую машину поиска выхода из лабиринта.</p>

<p>Шеннон уходит на пенсию в возрасте пятидесяти лет в 1966 году, но он продолжает консультировать компанию Белл (Bell Labs).</p>

<p>Он был разработчиком первой промышленной игрушки на радиоуправлении, которая выпускалась в 50-ые годы в Японии.</p>

