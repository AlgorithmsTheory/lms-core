<p>Теория связи в стойких системах</p>

<p>Работа Шеннона &laquo;Теория связи в секретных системах&raquo; (1945) с грифом секретно, которую рассекретили и опубликовали только лишь в 1949 году, послужила началом обширных исследований в теории кодирования и передачи информации, и, по всеобщему мнению, придала криптографии статус науки. Именно Клод Шеннон впервые начал изучать криптографию, применяя научный подход. В этой статье, Клод определил основополагающие понятия теории криптографии, без которых криптография уже немыслима. Важной заслугой Шеннона является исследования абсолютно стойких систем, и доказательство их существования, а также существование криптостойких шифров, и требуемые для этого условия. Шеннон также сформулировал основные требования, предъявляемые к надежным шифрам. Он ввел ставшие уже привычными понятия рассеивания и перемешивания, и методы создания криптостойких систем шифрования на основе простых операций. Данная статья является отправным пунктом изучения науки криптографии.</p>

<p>Математическая теория связи</p>

<p>Статья &laquo;Математическая теория связи&raquo;, была опубликована в 1948 году и сделала Клода Шеннона всемирно известным. В ней Шеннон изложил свои идеи, ставшие впоследствии основой современных теорий и техник обработки передачи и хранения информации. Результаты его работ в области передачи информации по каналам связи запустили по всему миру огромное число исследований. Шеннон обобщил идеи Хартли и ввел понятие информации, содержащейся в передаваемых сообщениях. В качестве меры информации передаваемого сообщения М, Хартли предложил использовать логарифмическую функцию. Шеннон первым начал рассматривать передаваемые сообщения и шумы в каналах связи с точки зрения статистики, рассматривая как конечные множества сообщений, так и непрерывные множества сообщений. Развитая Шенноном теория информации помогла решить главные проблемы, связанные с передачей сообщений, а именно: устранить избыточность передаваемых сообщений, произвести кодирование и передачу сообщений по каналам связи с шумами. Решение проблемы избыточности подлежащего передаче сообщения позволяет максимально эффективно использовать канал связи. К примеру, современные повсеместно используемые методы снижения избыточности в системах телевизионного вещания на сегодняшний день позволяют передавать до шести цифровых программ коммерческого телевидения, в полосе частот, которую занимает обычный сигнал аналогового телевидения. В своих работах Шеннон доказал принципиальную возможность решения проблем, связанных с передачей сообщений, это явилось в конце 40-х годов настоящей сенсацией в научных кругах. Данная работа, как и работы, в которых исследовалась потенциальная помехоустойчивость, дали начало огромному числу исследований, продолжающихся и по сей день, уже более полувека. Ученые из Советского Союза и США (СССР &mdash; Пинскер, Хинчин, Добрушин, Колмогоров; США- Галлахер, Вольфовиц, Фейнштейн) дали строгую трактовку изложенной Шенноном теории. На сегодняшний день все системы цифровой связи проектируются на основе фундаментальных принципов и законов передачи информации, разработанных Шенноном.</p>

<p>Большое количество исследований было посвящено созданию кодов, устойчивых к помехам, и простых методов декодирования сообщений. Исследования, проведенные за последние пятьдесят лет, легли в основу созданной Рекомендации Международного союза электросвязи по применению помехоустойчивого кодирования и методов кодирования источников информации в современных цифровых системах.</p>

<p>Теорема о пропускной способности канала.</p>

<p>Любой канал с шумом характеризуется максимальной скоростью передачи информации, этот предел назван в честь Шеннона. При передаче информации со скоростями, превышающими этот предел, происходят неизбежные искажения данных, но снизу к этому пределу можно приближаться с необходимой точностью, обеспечивая сколь угодно малую вероятность ошибки передачи информации в зашумленном канале.</p>

<p>Теоремы Шеннона</p>

<li>  Прямая и обратная теоремы Шеннона для источника общего вида &mdash; о связи энтропии источника и средней длины сообщений.</p>

<li>  Прямая и обратная теоремы Шеннона для источника без памяти &mdash; о связи энтропии источника и достижимой степени сжатия с помощью кодирования с потерями и последующего неоднозначного декодирования.</p>

<li>  Прямая и обратная теоремы Шеннона для канала с шумами &mdash; о связи пропускной способности канала и существования кода, который возможно использовать для передачи с ошибкой, стремящейся к нулю (при увеличении длины блока).</p>

<li>  Теорема отсчётов Уиттакера &mdash; Найквиста &mdash; Котельникова &mdash; Шеннона (теорема Котельникова) &mdash; об однозначном восстановлении сигнала по своим дискретным отсчётам.</p>

<li>  Теорема Шеннона об источнике шифрования (или теорема бесшумного шифрования) устанавливает предел максимального сжатия данных и числовое значение энтропии Шеннона.</p>

