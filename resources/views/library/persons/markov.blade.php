		<p><strong>Марков (мл.) Андрей Андреевич </strong>{!! HTML::image('img/library/persons/markov1.jpg', 'markov', array('style' => 'float:left; height:250px; width:188px')) !!}(1903&ndash;1979)</p>

<p><em>Родился 9(22) сентября 1903&nbsp;г., Санкт-Петербург.</em></p>

<p><em>Умер 13 октября 1979&nbsp;г., Москва.</em></p>

<p>Математик. Член-корреспондент АН СССР по Отделению физико-математических наук (математика) с 23&nbsp;октября 1953&nbsp;г.</p>

<p>Родился в семье А.&nbsp;А.&nbsp;Маркова (старшего). Унаследовал от отца выдающиеся математические способности и любовь к науке. Получил блестящее домашнее образование. Сначала поступил на химический факультет Петроградского университета, но с 3-го курса продолжил обучение на физическом отделении физико-математического факультета, который окончил в 1924&nbsp;г. В ту пору занимался теоретической физикой и прикладной геофизикой. В 1925&ndash;1928&nbsp;гг. обучался в аспирантуре Астрономического института. Диссертацию, представленную на соискание ученой степени кандидата физико-математических наук, защитил в 1928&nbsp;г.</p>

<p>&nbsp;В 1928&ndash;1935&nbsp;гг. А.&nbsp;А.&nbsp;Марков работал в Астрономическом институте. В 1933&ndash;1936&nbsp;гг. &mdash; старший научный сотрудник НИИ математики и механики ЛГУ. В 1935&nbsp;г. ему без защиты диссертации была присуждена ученая степень доктора физико-математических наук. Утвержден в звании профессора &nbsp;в 1936&nbsp;г. В 1933&ndash;1955&nbsp;гг. преподавал в Ленинградском государственном университете &mdash; профессор с 1936&nbsp;г.</p>

<p>В 1939&ndash;1953&nbsp;гг. А.&nbsp;А.&nbsp;Марков работал в ЛОМИ &mdash; старший научный сотрудник, заведующий лабораторией, заместитель директора в 1941&ndash;1953&nbsp;гг. (сначала по МИАН в целом, с 1943&nbsp;г. после реэвакуации из Казани &mdash; по ЛОМИ). В период Великой Отечественной войны работал в коллективе МИАН, эвакуированном в Казань. После реэвакуации возвратился в Ленинград.</p>

<p>В 1954&nbsp;г. А.&nbsp;А.&nbsp;Марков переехал в Москву. В 1954&ndash;1972&nbsp;гг. работал в МИАН &mdash; заведующий лабораторией в 1954&ndash;1972&nbsp;гг., несколько лет работал на посту заместителя директора.</p>

<p>С 1964&nbsp;г. до конца жизни А.&nbsp;А.&nbsp;Марков &mdash; заведующий лабораторией математической логики и структуры машин Вычислительного центра АН СССР. С 1959&nbsp;г. до конца жизни &mdash; профессор, заведующий кафедрой математической логики и теории алгоритмов механико-математического факультета МГУ. На руководимой им кафедре работал С.&nbsp;В.&nbsp;Яблонский.</p>

<p>Основные направления математических исследований А.&nbsp;А.&nbsp;Маркова &mdash; топология, топологическая алгебра, теория динамических систем, теория алгоритмов, математическая логика и конструктивная математика.</p>

<p>В ранний период творческой деятельности (в 1928&ndash;1935&nbsp;гг., когда работал в Астрономическом институте) А.&nbsp;А.&nbsp;Марков выполнил ряд работ по небесной механике, которые сохраняют значение до настоящего времени. Небесная механика естественным образом привела его к чисто математической проблематике.</p>

<p>Диапазон математических дисциплин, которые А.&nbsp;А.&nbsp;Марков охватил в процессе своего творчества, чрезвычайно широк &mdash; от аксиоматической теории множеств до теории пластичности. В начале 1930-х&nbsp;гг. выполнил цикл работ по общей теории динамических систем. Им впервые было сформулировано общее, не зависящее от дифференциальных уравнений, определение динамической системы &mdash; важнейшего понятия современной теоретической кибернетики.</p>

<p>В 1940&ndash;1946&nbsp;гг. А.&nbsp;А.&nbsp;Марков выполнил ряд работ по свободным топологическим группам. Им было получено доказательство неразрешимости проблемы гомеоморфизма в топологии.</p>

<p>В области математической логики А.&nbsp;А.&nbsp;Марков решил две знаменитые математические проблемы &mdash; проблему тождества для полугрупп (так называемая проблема Туэ) и проблему гомеоморфии в топологии. Создал рабочий аппарат, опирающийся на удачно выбранное им понятие нормальных алгоритмов (использовал название &laquo;алгорифмы&raquo;, теперь их принято называть алгоритмами Маркова). Показал, что это понятие обладает принципиальными и методическими достоинствами. Введенное им понятие выдержало испытание временем и прочно вошло в научный обиход как общей теории алгоритмов, так и теоретической кибернетики, в которой послужило источником точной постановки ряда проблем. Методика, разработанная А.&nbsp;А.&nbsp;Марковым при построении теории нормальных алгоритмов, в значительной степени предвосхитила приемы структурного программирования и технику верификации программ.</p>

<p>А.&nbsp;А.&nbsp;Марков получил ответ на вопрос об инверсионной сложности булевых функций и нашел минимальные контактно-вентильные схемы, реализующие симметрические булевы функции. Заложил основы теории сложности алгоритмов. Развил специальную конструктивную логику, учитывающую специфику конструктивных объектов.</p>

<p>А.&nbsp;А.&nbsp;Марков занимался также прикладными вопросами математической логики, в частности ее применением в теории вычислительных машин. Разработал терминологию для описания работы вычислительных машин. Имеет многочисленные работы в области криптографии. Наиболее известна &laquo;теорема Маркова&raquo;, которая классифицирует шифры, не распространяющие искажения.</p>

