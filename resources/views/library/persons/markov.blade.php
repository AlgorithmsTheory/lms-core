<p>А.&nbsp;А.&nbsp;Марков основал большую и продуктивно работающую научную школу математической логики. С 1943&nbsp;г. руководил (совместно с С.&nbsp;Л.&nbsp;Яновской) в МГУ научным семинаром по математической логике. Его преемником в этой деятельности в дальнейшем стал П.&nbsp;С.&nbsp;Новиков.</p>

<p>С 1964&nbsp;г. до конца жизни А.&nbsp;А.&nbsp;Марков &mdash; член Научного совета по кибернетике АН СССР. С 1969&nbsp;г. &mdash; член комитета Советского национального объединения истории и философии естествознания и техники. В 1976&ndash;1979&nbsp;гг. &mdash; вице-президент Московского математического общества. С 1969&nbsp;г. &mdash; член комитета Советского национального объединения истории и философии естествознания и техники. С 1976&nbsp;г. &mdash; вице-президент Московского математического общества.</p>

<p>Андрей Андреевич Марков &mdash; яркий представитель группы ученых, творческая деятельность которых развивалась в период становления Математического института им. В.&nbsp;А.&nbsp;Стеклова АН СССР и была ориентирована в значительной степени на развитие новых направлений математики. Своим творчеством не только содействовал укреплению прочных позиций МИАН в топологии, но и выступил создателем новых научных направлений &mdash; теории алгоритмов и конструктивной математики.</p>

<p>Высшим периодом его научной деятельности является последний, длившийся более тридцати лет период интенсивных исследований в области математической логики.</p>

<p>Унаследовал от отца &mdash; А.&nbsp;А.&nbsp;Маркова (старшего) не только любовь к математике, но и настоящее гражданское мужество. Оно было ему надежной основой в период нелегкой борьбы за право кибернетики на самостоятельное развитие и возможность беспрепятственно заниматься связанными с ней научными исследованиями. А.&nbsp;А.&nbsp;Марков (младший) был в числе ученых, много сил отдавших этой нелегкой, но справедливой борьбе и своими научными трудами обеспечивших торжество справедливости.</p>

