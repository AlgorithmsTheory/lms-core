<p><strong>Бертран Рассел</strong><br />
(18 мая 1872 - 2 февраля 1970)</p>

<p>{!! HTML::image('img/library/persons/rassel1.jpg', 'rassel', array('style' => 'float:left; height:300px; width:192px')) !!}<strong>Детство</strong></p>

<p>Бертран Артур Уильям Рассел родился в Треллеке (Уэльс) 18 мая 1872 года, он принадлежал к старинному аристократическому роду политиков, учёных и интеллектуалов. Уже к своему четвёртому дню рождения Рассел стал полным сиротой. После смерти обоих родителей, Бертрана и двух его старших братьев взяла на попечение их бабушка, графиня Рассел.</p>


<p><strong>Молодость</strong></p>

<p>В декабре 1889 г. Бертран Рассел поступает в Тринити-колледж.</p>

<p>Рассела, сына лорда одного из самых влиятельных родов, назначают представителем Великобритании сначала в Париже, потом в Берлине. В Германии Рассел изучает практически весь диапазон немецкой философии, среди прочего экономические труды Маркса. Рассел проникается идеями левого реформизма.</p>

<p><strong>Первая мировая война</strong></p>

<p>Пока Англия готовилась к войне, Рассел убеждался в верности пацифизма, основой для которого послужил для Рассела его социализм. За противодействие властям Рассела лишают места в Тринити-колледже, но больше всего Рассел расстраивается из-за ссор со многими друзьями, для которых был неприемлем пацифизм в условиях угрозы Великобритании. Рассел высказал мысль о том, что политическая свобода в Англии становится фарсом. За это власти привлекают его к суду. Рассел заявил, что на скамье подсудимых оказался не только он, но и вся традиционная британская свобода. В результате судебных разбирательств Рассела оштрафовали на 100 фунтов стерлингов, конфисковали библиотеку и не позволили поехать в США для чтения лекций.</p>

<p><strong>Поездка в Советскую Россию</strong></p>

<p>19 мая 1920 Рассел в составе лейбористской делегации отправился в Советскую Республику и пробыл там по 17 июня 1920 г. Рассел признал советскую модель развития не соответствующей истинно коммунистическим идеям и в значительной степени разочаровался в большевиках.</p>

<p><strong>Поездка в Китай</strong></p>

<p>По приглашению &laquo;Общества новых учений&raquo;, организованном лидером реформаторского движения Лян Цичао 12 октября 1920 г. Рассел отправляется в Китай, где пробыл по 10 июня 1921 г. Философия Лосы, как называли Рассела в Китае, оказала сильное воздействие на передовую молодёжь в период антиимпериалистического &laquo;Движения 4 мая&raquo;.</p>

<p><strong>Вторая мировая война</strong></p>

<p>Приближение войны порождает у Рассела сильные сомнения в целесообразности пацифизма.</p>

<p>{!! HTML::image('img/library/persons/rassel3.jpg', 'rassel', array('style' => 'float:left; height:402px; width:322px')) !!}&nbsp;&nbsp;&nbsp;&nbsp; &laquo;Хотя я и придерживался пацифистских убеждений, но это давалось мне все с большим трудом. Когда в 1940 году Англии угрожала опасность оккупации, я понял, что на протяжении всей первой мировой ни разу всерьёз не допускал мысли о поражении. Мысль о нем была невыносима, и после серьезных размышлений я решил, что должен выступать в поддержку всего, что делается ради победы, как бы тяжело ни далась эта победа и каковы бы ни были ее последствия.&raquo;</p>

<p>Вплоть до 1954 г. Рассел поддерживает политику холодной войны, будучи убежденным в том, что это может предотвратить Третью мировую войну. За содействие официальному режиму и пропаганде взглядов Великобритании относительно холодной войны, 9 Июня 1949 Рассела награждают орденом &laquo;За заслуги&raquo;.</p>

<p>В 1950 году 78-летнему Расселу присуждают Нобелевскую премию по литературе за книгу &laquo;Брак и мораль&raquo; (1929) и публицистическую деятельность.</p>

