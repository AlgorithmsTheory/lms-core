<p><strong style="line-height:1.6em">Множество упорядоченных n-ок натуральных чисел</strong></p>

<p style="margin-left:36.0pt"><strong><em><a name="72">Упорядоченная </em></strong><strong><em>n</em></strong><strong><em>-ка натуральных чисел</a></em></strong><em> &ndash; это набор из </em><em>n</em><em> элементов вида (</em><em>m</em><em><sub>1</sub></em><em>, </em><em>m</em><em><sub>2</sub></em><em>, &hellip;, </em><em>m<sub>n</sub></em><em>), где </em><em>m<sub>i</sub></em> <em>&ndash; натуральное число.</em></p>

<p style="margin-left:36.0pt">&nbsp;</p>

<p><strong><u>Теорема</u></strong></p>

<p><strong>Множество упорядоченных </strong><strong>n</strong><strong>-ок натуральных чисел счетно и эффективно перечислимо.</strong></p>

<p style="margin-left:63.0pt"><strong style="line-height:1.6em"><u>Доказательство</u></strong></p>

<p>Чтобы установить взаимно-однозначное соответствие между упорядоченными&nbsp; n-ками натуральных чисел и натуральными числами, достаточно расположить разложить n-ку вида (m<sub>1</sub>, m<sub>2</sub>, m<sub>3</sub>,&hellip;,m<sub>n</sub>) следующим образом:</p>

<p>(m<sub>1</sub>, m<sub>2</sub>, m<sub>3</sub>,&hellip;,m<sub>n</sub>) = (m<sub>1</sub>, (m<sub>2</sub>, m<sub>3</sub>,&hellip;,m<sub>n</sub>)) = (m<sub>1</sub>, (m<sub>2</sub>, (m<sub>3</sub>,&hellip;,m<sub>n</sub>))) = &nbsp;=(m<sub>1</sub>, (m<sub>2</sub>, (m<sub>3</sub>, (&hellip;(m<sub>n</sub><sub>-1</sub>,m<sub>n</sub>)))))</p>

<p>Расположив по горизонтали таблицы пары натуральных чисел, а по вертикали &ndash; натуральные числа, диагональным методом получим нумерацию троек натуральных чисел. Далее по горизонтали таблицы располагаются тройки натуральных чисел, а по вертикали &ndash; натуральные числа, диагональным методом получаем нумерацию четверок натуральных чисел и т.д.</p>

<p>Таким образом доказано, что множество n-ок натуральных чисел равномощно множеству N, а значит оно <strong><em>счетно</em></strong>.</p>

<p>Факт <strong><em>эффективной перечислимости</em></strong> множества напрямую следует из приведенного способа&nbsp; нумерации элементов натуральными числами. Итак, множество упорядоченных n-ок натуральных чисел счетно и эффективно перечислимо, <strong>Q.E.D.</strong></p>

