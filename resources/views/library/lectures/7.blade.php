<p><strong style="line-height:1.6em">Множество упорядоченных пар натуральных чисел</strong></p>

<p style="margin-left:54.0pt"><em style="line-height:1.6em">Два элемента </em><em style="line-height:1.6em">a</em><em style="line-height:1.6em"> и </em><em style="line-height:1.6em">b</em><em style="line-height:1.6em"> называют<strong> <a name="71">упорядоченной парой</a></strong>, если указано, какой их этих элементов первый, а какой второй и при этом ((</em><em style="line-height:1.6em">a</em><em style="line-height:1.6em">,</em><em style="line-height:1.6em">b</em><em style="line-height:1.6em">)=(</em><em style="line-height:1.6em">c</em><em style="line-height:1.6em">,</em><em style="line-height:1.6em">d</em><em style="line-height:1.6em">))&lt;=&gt;(</em><em style="line-height:1.6em">a</em><em style="line-height:1.6em">=</em><em style="line-height:1.6em">c</em><em style="line-height:1.6em">)^(</em><em style="line-height:1.6em">b</em><em style="line-height:1.6em">=</em><em style="line-height:1.6em">d</em><em style="line-height:1.6em">). Упорядоченную пару элементов обозначают (</em><em style="line-height:1.6em">a</em><em style="line-height:1.6em">,</em><em style="line-height:1.6em">b</em><em style="line-height:1.6em">).</em></p>

<p style="margin-left:36.0pt"><strong style="line-height:1.6em"><u>Теорема</u></strong></p>

<p><strong>Множество упорядоченных пар натуральных чисел счетно и эффективно перечислимо.</strong></p>

<p style="margin-left:36.0pt"><strong><u>Доказательство</u></strong></p>

<p>Обычно, употребляя термин &laquo;упорядоченная&raquo; пара считают, что допустим пара (1,5) и пара (5,1) имеют разный смысл и рассматриваются как различные. Чтобы установить взаимно-однозначное соответствие между упорядоченными парами натуральных чисел и натуральными числами, достаточно расположить пары (p,q) в таблицу так, что&nbsp; (p,q) находится в p-ой строке и в q-ом столбце.</p>

