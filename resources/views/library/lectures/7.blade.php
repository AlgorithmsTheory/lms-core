<p><span style="line-height:1.6em">Все (и положительные, и отрицательные) рациональные числа в совокупности перечисляются по аналогии с целыми числами, путем чередования положительной дроби и её отрицательного аналога. При этом некоторые рациональные числа мы нумеруем по нескольку раз: например, 1 будет пронумерована как 1/1, 2/2, и т.д., а например 4/5 как 8/10, 12/15 и т.д.</span></p>

<p>Т.о., показано, что множество рациональных чисел не превосходит по мощности множество натуральных чисел, |Q|&le;|N|, т.к. каждое рациональное число получит соответствующий номер, а если быть точным &ndash; то даже несколько номеров. С другой стороны то, что множество натуральных чисел не превосходит по мощности множество рациональных чисел очевидно, |N|&le;|Q| (хотя бы потому, что оно является его подмножеством). Т.о. доказано, что множество рациональных чисел равномощно множеству натуральных чисел |Q|=|N| = &alefsym;<sub>0</sub><sub>, </sub>&nbsp;а значит оно <strong><em>счетно</em></strong>.</p>

<p>Факт <strong><em>эффективной перечислимости</em></strong> множества Q напрямую следует из приведенного способа&nbsp; нумерации элементов натуральными числами. В ходе этой нумерации каждое рациональное&nbsp; число получает соответствующий номер, и если к алгоритму добавить процедуру, проверяющую дробь на предмет сокращаемости (если числитель и знаменатель имеют общие делители) и исключающую из нумерации сокращаемые дроби, то мы в чистом виде получим перечисление рациональных чисел&nbsp; по алгоритму без пропусков и повторений, что совпадает с определением эффективной перечислимости. Итак, множество рациональных чисел счетно и эффективно перечислимо, <strong>Q.E.D.</strong></p>

<p>Это вероятно следующий, после парадоксов&nbsp; Галилея и Гильберта, не вполне очевидный и по началу воспринимаемый как парадоксальный, факт. Ведь множество рациональных чисел расположено на прямой повсюду плотно, и между любыми двумя рациональными числами существует другие рациональные числа, однако мощность множества таких чисел оказывается не больше, чем у множества целых или натуральных чисел.</p>

<p><strong style="line-height:1.6em">Множество действительных алгебраических чисел</strong></p>

<p style="margin-left:36.0pt"><strong style="line-height:1.6em"><em><a name="75">Алгебраическое действительное число</a></em></strong><span style="line-height:1.6em"> &ndash;</span><em style="line-height:1.6em"> действительный корень алгебраического уравнения ненулевой степени с рациональными коэффициентами.</em></p>

<p><span style="line-height:1.6em">Множество алгебраических действительных чисел обозначим латинской буквой А.</span></p>

<p>&nbsp;Общий вид алгебраического уравнения:&nbsp; a<sub>0</sub>+a<sub>1</sub>∙x<sup>1</sup>+a<sub>2</sub>∙x<sup>2</sup>+&hellip;+a<sub>n</sub>∙x<sup>n</sup>=0, где a<sub>0</sub>, a<sub>1</sub>,&hellip;- рациональные коэффициенты. Исходя из определения, можно утверждать, что рассмотренные ранее классы натуральных, целых и рациональных чисел являются подмножествами множества алгебраических чисел.</p>

<p><strong style="line-height:1.6em"><u>Теорема</u></strong></p>

<p><strong>Множество алгебраических чисел счетно и эффективно перечислимо.</strong></p>

<p style="margin-left:36.0pt"><strong style="line-height:1.6em"><u>Доказательство</u></strong></p>

<p>Доказательство построим привычным образом, а именно предложим процедуру нумерации всех алгебраических чисел числами натурального ряда. При этом каждое число будем задавать через образующее его алгебраическое уравнение. Так, для линейных уравнений будем иметь упорядоченные пары рациональных чисел, для квадратных уравнений &ndash; тройки, в общем случае получаем упорядоченную n-ку рациональных чисел: (a<sub>i</sub><sub>1</sub>,a<sub>i</sub><sub>2</sub>&hellip;a<sub>in</sub>) для каждого i-ого алгебраического уравнения (n-1)-ой степени. Располагать элементы будем в двусторонне бесконечной матрице.</p>

<p>Выпишем на первой строке будущей матрицы все упорядоченные пары рациональных чисел. Это возможно, т.к. пары рациональных чисел эффективно перечислимы (рациональные числа эффективно перечисляются, их можно записать в матрицу и перечислить пары чисел диагональным способом). Такие пары рациональных чисел соответствуют линейным уравнениям и имеют по одному корню: т.о. каждая пара однозначно определяет корень линейного уравнения.</p>

<p>На второй строке выпишем все упорядоченные тройки рациональных чисел. Это возможно, т.к. тройки рациональных чисел эффективно перечислимы (рациональные числа эффективно перечисляются, их пары тоже эффективно перечисляются, значит можно записать в матрицу по строкам пары, по столбцам числа и перечислить тройки чисел диагональным способом). Такие тройки соответствуют квадратным уравнениям и имеют максимум по два корня: таким образом, в процессе формирования матрицы каждую тройку рациональных чисел нужно будет повторить два раза для обеспечения процесса получения соответствующего номера для двух чисел, являющихся решением соответствующего уравнения.</p>

<p>На третьей строке &ndash; по три числа на каждое кубическое уравнение соотв. упорядоченным четверкам и т.д.</p>

<p>Т.о. получим матрицу, которую можно обойти при помощи диагонального процесса Кантора. Если часть корней алгебраического уравнения комплексная, при нумерации их просто пропускаем. Т.о. каждое алгебраическое число получит соответствующий номер, и это подтверждает тот факт, что множество алгебраических действительных чисел <strong><em>счетно</em></strong>.</p>

<p>Факт <strong><em>эффективной перечислимости</em></strong> множества А напрямую следует из приведенного способа&nbsp; нумерации элементов натуральными числами, т.к.&nbsp; попутно указана эффективная процедура нумерации наборов рациональных чисел, однозначно задающих алгебраические уравнения соответствующей степени. При этом важно то, что алгебраическое уравнение n-ой степени имеет эффективный алгоритм решения, т.о. процедура полностью эффективна. Итак, множество алгебраических действительных чисел счетно и эффективно перечислимо, <strong>Q.E.D.</strong></p>

<p>Счетными также будут множества, составленные из всех пар, троек, и т.д. алгебраических чисел.</p>

<p><strong style="line-height:1.6em">Счетные числовые множества: обобщение</strong></p>

<p><strong><u>Теорема (без доказательства)</u></strong></p>

<p><strong>Множество элементов, которые можно представить с помощью конечного числа счетной системы знаков, счетно.&nbsp; </strong></p>

