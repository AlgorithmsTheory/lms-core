<p style="margin-left:36.0pt"><strong style="line-height:1.6em"><u>Теорема</u></strong></p>

<p><strong>Множество рациональных чисел счетно и эффективно перечислимо.</strong></p>

<p style="margin-left:63.0pt"><strong style="line-height:1.6em"><u>Доказательство</u></strong></p>

<p>Обозначим множество рациональных чисел Q.</p>

<p>Рассмотрим сначала положительные рациональные числа &ndash; множество Q<sup>+</sup>. Определим положительное рациональное число как q=n/m, где n и m &ndash; натуральные числа.</p>

<p>Запишем их в виде бесконечной матрицы, строки и столбцы которой пронумерованы натуральными числами начиная с 1. Элемент, стоящий на пересечении i-ой строки и&nbsp; j-ого столбца, получит наименование <strong><em>q<sub>ij</sub></em></strong></p>

<p>Используя диагональный метод, перечислим их (пронумеруем натуральными числами):</p>

