<p><span style="line-height:1.2em">Счетные множества</span></p>

<p><span style="line-height:1.6em">Счетно-бесконечными также будут все множества, для которых удастся доказать равномощность с множеством натуральных чисел. Далее в этой главе для краткости и соответствия общепринятым формулировкам теорем, вместо термина &laquo;счетно-бесконечные&raquo; при доказательстве равномощности рассматриваемого множества и множества натуральных чисел будет использоваться термин &laquo;счетные&raquo;. Это не является ошибочным утверждением, любое счетно-бесконечное множество является счетным, но не наоборот. Строго говоря, доказать факт того, что множество счетное проще, чем доказать том факт, что оно счетно-бесконечное (в последнем случае требуется показать, что множество не является конечным).</span></p>

<p>Для доказательства того, что множества равномощны, обычно используется какой либо способ, позволяющий поставить в соответствие каждому элементу рассматриваемого множества какое-то натуральное число. Подобный прием использовался при доказательстве Теорем 2.2.(1) и 2.2.(2). В общем случае оказывается вовсе необязательным конкретное указание эффективного способа установления такого соответствия. Достаточно доказательства самого факта. Более того, если в процессе доказательства равномощности такой (обязательно эффективный, т.е. основанный на алгоритме) способ будет найден, то помимо собственно требуемого доказательства счетности, попутно будет доказан факт эффективной перечислимости исследуемого множества. При этом уже становится обязательным наличие процедуры, которая устанавливает взаимно - однозначное соответствие между элементами исследуемого множества и элементами множества натуральных чисел.</p>

<p>Помимо указанного способа, зачастую используется методика оценки кардинального числа множества сверху и снизу, что зачастую позволяет точно вычислить реальное значение мощности исследуемого множества.</p>

<p>В дальнейшем в ряде задач рассматривается &laquo;расширенное&raquo; множество натуральных чисел, включающее в себя стандартный ряд натуральных чисел (1,2,3,&hellip;) и число 0. Доказательство равномощности этих множеств не составляет труда. Будем обозначать множество натуральных&nbsp; чисел буквой N, а расширенное множество натуральных чисел N*.</p>

<p>Обычное и расширенное множество натуральных чисел являются эффективно перечислимыми (первое по определению, второе по причине простейшего установления нумерации 0-&gt;1, 1-&gt;2, 2-&gt;3,&hellip;.и т.д., позволяющей установить взаимно-однозначное соответствие между элементами исследуемого множества и элементами множества натуральных чисел).</p>

<p>Стоит также отметить, что любое конечное множество также эффективно перечислимо. Важно обратить особое внимание на тот факт, что исходя из сформулированных определений, счетность конкретного множества вовсе не означает, что это множество гарантированно будет эффективно перечислимым. Более того, как будет показано в последующем, найдутся множества, являющиеся счетными и эффективно не перечислимыми одновременно.</p>

<p><strong style="line-height:1.6em">Множество целых чисел</strong></p>

<p style="margin-left:36.0pt"><strong><em><a name="70">Множество целых чисел</a></em></strong><em> &ndash; множество, состоящее из натуральных чисел, числа ноль и чисел, построенных на основе натуральных только со знаком &laquo;минус&raquo; (отрицательных чисел).</em></p>

<p style="margin-left:36.0pt"><strong style="line-height:1.6em"><u>Теорема</u></strong></p>

<p><strong>Множество целых чисел счетно и эффективно перечислимо.</strong></p>

<p style="margin-left:36.0pt"><strong><u>Доказательство</u></strong></p>

<p>Ряд целых чисел: -n, &hellip;, -3,-2,-1,0,1,2,3,&hellip;, n,&hellip;Будем обозначать множество целых чисел буквой Z. Расположим целые числа следующим образом:</p>

<p>0, 1, -1, 2, -2, 3, -3, &hellip;., n, -n, &hellip;</p>

<p>Тогда каждому числу можно поставить в соответствие натуральное число</p>

<p>0, &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 1, &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; -1, &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 2, &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; -2, &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 3, &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &hellip;., n,&nbsp; -n, &hellip;</p>

<p>1,&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 2, &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 3,&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 4, &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 5,&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 6,&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &hellip;., 2n, 2n+1, &hellip;</p>

<p>Таким образом доказано, что&nbsp; множество Z равномощно множеству N, а значит оно <strong><em>счетно</em>.</strong></p>

<p>Для доказательства эффективной перечислимости множества Z необходимо установить тот факт, что все элементы множества Z могут быть перебраны по алгоритму и должны получить в результате такого перебора порядковые номера, без пропусков и повторений.</p>

<p>Факт <strong><em>эффективной перечислимости</em></strong> множества Z напрямую следует из приведенного способа&nbsp; нумерации элементов натуральными числами. Итак, множество Z счетно и эффективно перечислимо, <strong>Q.E.D.</strong></p>

