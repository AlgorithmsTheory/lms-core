<p>&alefsym;<sub>0</sub> +&alefsym;<sub>0</sub> <sup>2</sup>+&alefsym;<sub>0</sub><sup>3</sup> +&hellip;+&alefsym;<sub>0</sub><sup>k</sup>= &sum;&alefsym;<sub>0</sub><sup>k</sup> =&sum;&alefsym;<sub>0</sub>&nbsp; = k &bull;&alefsym;<sub>0</sub> = &alefsym;<sub>0</sub></p>

<p><strong style="line-height:1.6em">Множество рациональных чисел</strong></p>

