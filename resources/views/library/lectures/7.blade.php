<p><strong style="line-height:1.6em">Множество конечных комплексов натуральных чисел</strong></p>

<p style="margin-left:36.0pt"><strong><em><a name="73">Конечные комплексы натуральных чисел</a></em></strong><em>&nbsp; - это элементы вида (</em><em>p</em><em><sub>1</sub></em><em>), (</em><em>p</em><em><sub>1</sub></em><em>, </em><em>p</em><em><sub>2</sub></em><em>), (</em><em>p</em><em><sub>1</sub></em><em>, </em><em>p</em><em><sub>2</sub></em><em>, </em><em>p</em><em><sub>3</sub></em><em>), &hellip;,&nbsp; (</em><em>p</em><em><sub>1</sub></em><em>, </em><em>p</em><em><sub>2</sub></em><em>, &hellip;, </em><em>p<sub>k</sub></em><em>), где </em><em>k</em><em> и </em><em>p<sub>i</sub></em>&nbsp; <em>пробегают все натуральные числа.</em></p>

<p><strong style="line-height:1.6em"><u>Теорема</u></strong></p>

<p><strong>Множество конечных комплексов натуральных чисел счетно и эффективно перечислимо.</strong></p>

<p style="margin-left:63.0pt"><strong style="line-height:1.6em"><u>Доказательство</u></strong></p>

<p style="margin-left:36.0pt">Чтобы установить взаимно-однозначное соответствие между конечными комплексами натуральных чисел и натуральными числами, можно использовать двоичное разложение вида:</p>

<p>n=2^(p<sub>1</sub>-1) + 2^(p<sub>1</sub>+p<sub>2</sub>-1)+ &hellip;+2^(p<sub>1</sub>+p<sub>2</sub>+ &hellip;+p<sub>k</sub> -1), где ^ - значок степени.</p>

<p>Например, в двоичном коде 27 = 11011= 1&bull;2<sup>0</sup> + 1&bull;2<sup>1</sup> +0&bull;2<sup>2</sup> +1&bull;2<sup>3</sup> +1&bull;2<sup>4 </sup>&nbsp;= 2<sup>0</sup> + 2<sup>1</sup> +2<sup>3</sup> + 2<sup>4</sup>, откуда получим:</p>

<p>p<sub>1</sub>-1 = 0 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; p<sub>1</sub>=1&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp; p<sub>1</sub>=1</p>

<p>p<sub>1</sub>+ p<sub>2</sub> -1 = 1&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; p<sub>1</sub>+ p<sub>2</sub>=2&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; p<sub>2</sub>=1</p>

<p>p<sub>1</sub>+ p<sub>2</sub> + p<sub>3</sub> -1 = 3&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; p<sub>1</sub>+ p<sub>2</sub> + p<sub>3</sub> = 4&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; p<sub>3</sub> =2</p>

<p>p<sub>1</sub>+ p<sub>2</sub> + p<sub>3</sub> + p<sub>4</sub> -1 = 4&nbsp;&nbsp;&nbsp;&nbsp; p<sub>1</sub>+ p<sub>2</sub> + p<sub>3</sub>+ p<sub>4</sub> = 5&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; p<sub>4</sub> =1</p>

<p>Итак, натуральное число 27 является кодом комплекса (1,1,2,1).</p>

<p>В свою очередь комплексу&nbsp; (2,1,1,1) соответствует следующий код:</p>

<p>p<sub>1</sub>-1 = 2 -1 = 1&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>

<p>p<sub>1</sub>+ p<sub>2</sub> -1 = 2 + 1 &ndash; 1 = 2&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>

<p>p<sub>1</sub>+ p<sub>2</sub> + p<sub>3</sub> -1 = 2 + 1 - 1 -1 = 3</p>

<p>p<sub>1</sub>+ p<sub>2</sub> + p<sub>3</sub> + p<sub>4</sub> -1 = 2 + 1 &ndash; 1 + 1 &ndash; 1 = 4</p>

<p>В итоге число n = 2<sup>1</sup> + 2<sup>2</sup> + 2<sup>3</sup> + 2<sup>4 </sup>&nbsp;= 11110 (в двоичном коде) или 2 + 4 + 8 + 16 = 30 ( в десятичном коде). Таким образом, комплексу (2,1,1,1) соответствует натуральное число 30.</p>

<p>В результате доказано, что множество конечных комплексов натуральных чисел равномощно множеству N, а значит оно <strong><em>счетно</em></strong>.</p>

<p>Факт <strong><em>эффективной перечислимости</em></strong> множества напрямую следует из приведенного способа&nbsp; нумерации элементов натуральными числами. Итак, множество конечных комплексов натуральных чисел счетно и эффективно перечислимо, <strong>Q.E.D.</strong></p>

