<p>Таким образом доказано, что&nbsp; множество упорядоченных пар натуральных чисел равномощно множеству N, а значит, оно <strong><em>счетно</em></strong>.</p>

<p>Иногда под термином &laquo;<strong><em>упорядоченные</em></strong>&raquo; понимают ситуацию, при которой в паре&nbsp; (p,q) например, гарантированно p &le; q, т.е. первый член пары меньше или равен второму. В этом случае пары (p,q) и&nbsp; (q,p) считаются тождественными, т.е. пара воспринимается как неупорядоченное множество из двух элементов, в котором на первом месте пишется меньшее число, а на втором &ndash; большее. Множество таких пар является собственным подмножеством множества рассмотренных выше пар и по логике вещей тем более будет <strong><em>счетно</em></strong>.</p>

<p>Факт <strong><em>эффективной перечислимости</em></strong> множества упорядоченных пар натуральных чисел независимо от конкретной трактовки термина &laquo;упорядоченный&raquo; представляется вполне очевидным. В первом случае он напрямую следует из приведенного способа&nbsp; нумерации элементов натуральными числами. Во втором случае к предложенному алгоритму перечисления необходимо добавить процедуру проверки соотношения между элементами p и q, и если, например, p&le;q, то присваивать очередной номер этой паре, а в противном случае пропускать её. Итак, множество упорядоченных пар натуральных чисел счетно и эффективно перечислимо, <strong>Q.E.D.</strong></p>

